/*
    # les tableaux 
    Un tableau est une structure de données séquentielle / linéaire qui contient une séquence finie d'éléments auxquels
    on peut accéder efficacement par leur position, ou indice.

    # Performance des opérations
    Le temps d'accès à un élément par son index est constant (O(1)), quel que soit l'élément. En effet, les éléments d'un tableau 
    sont contigus dans l'espace mémoire.

    
*/